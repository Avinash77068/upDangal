export function getArticlesByCategory(slug: string, limit?: number): Article[] {
  const filtered = articles.filter((a) => a.categorySlug === slug)
  return limit ? filtered.slice(0, limit) : filtered
}

export function getTrendingArticles(limit = 5): Article[] {
  return articles.filter((a) => a.isTrending).slice(0, limit)
}

export function getBreakingArticles(): Article[] {
  return articles.filter((a) => a.isBreaking)
}

export function getFeaturedArticle(): Article {
  return articles[0]
}

export function getHeroSideArticles(): Article[] {
  return articles.slice(1, 5)
}
